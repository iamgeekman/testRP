function micSet(state){
	if(state){
		$('.mic').removeClass('fa-microphone-slash');
		$('.mic').addClass('fa-microphone');
	} else {
		$('.mic').addClass('fa-microphone-slash');
		$('.mic').removeClass('fa-microphone');
	}
}
